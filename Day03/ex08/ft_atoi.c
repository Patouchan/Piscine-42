int		ft_atoi(char *str)
{
	int i;;
	int b;
	int c;

	i = 0;
	b = 0;
	c = 0;
	while (str[i]=='\v'|| str[i] =='\t'|| str[i] == '\f'||
	str[i] == '\n' || str[i] == '\r' || str[i] == '.')
		i++;
	if(str[i] == '_')
		b = 1;
	while (str[i] == '-' || str[i] == '+')
		i++;

	return(0);
}


