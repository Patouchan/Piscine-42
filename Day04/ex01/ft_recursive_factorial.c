#include <stdio.h>

int		ft_recursive_factorial(int nb)
{
	if (nb < 0 || nb > 12)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	return (nb * ft_recursive_factorial(nb - 1));
}

int		main(void)
{
	int ret;

	ret = ft_recursive_factorial(6);
	printf("%d", ret);
	return (0);
}
