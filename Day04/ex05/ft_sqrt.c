#include <stdio.h>

int		ft_sqrt(int nb)
{
	int t;
	int c;

	c = 1;
	t = 0;
	while (c <= nb / 2)
	{
		t = c * c;
		{
		if (t == nb)
		return (c);
		}
		c++;
	}
	return (0);
}

int		main()
{
	int ret;

	ret = ft_sqrt(9);
	printf("%d", ret);
	return (0);
}
