int		ft_putchar(char c);

void	ft_print_comb2(void)
{
	int d;
	int e;

	d = 0;
	e = 0;
	while (d != 98 && e != 100)
	{
		e++;
		if (e > 99)
		{
			d++;
			e = d + 1;
		}
		ft_putchar((d / 10) + '0');
		ft_putchar((d % 10) + '0');
		ft_putchar(' ');
		ft_putchar((e / 10) + '0');
		ft_putchar((e % 10) + '0');
		if (!(d == 98 && e == 99))
		{
			ft_putchar(',');
			ft_putchar(' ');
		}
	}
}
