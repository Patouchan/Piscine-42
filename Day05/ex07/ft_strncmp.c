int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int t;

	t = 0;
	while (t < n)
	{
		if (*s1 != *s2)
			return (*s1 - *s2);
		if (*s1 == '\0' && *s2 == '\0')
			return (0);
		s1++;
		s2++;
		t++;
	}
	return (0);
}
