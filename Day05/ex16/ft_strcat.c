char		*ft_strcat(char *dest, char *src)
{
	int k;
	int s;

	k = 0;
	s = 0;
	while (dest[k])
	{
		k++;
		while (src[s])
			s++;
		dest[k] = src[s];
		k++;
		s++;
	}
	dest[k] = '\0';
	return (dest);
}
